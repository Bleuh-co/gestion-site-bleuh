// MESSAGES = dico de base + chaînes des modules métier (généré par le workflow
// i18n-retrofit). Spread : en cas de doublon, la clé MODULE l'emporte (valeur
// alignée sur le texte réellement affiché par le composant).
export const MESSAGES: Record<Lang, Record<string, string>> = {
  fr: { ...BASE_MESSAGES.fr, ...MODULE_MESSAGES.fr },
  en: { ...BASE_MESSAGES.en, ...MODULE_MESSAGES.en },
  es: { ...BASE_MESSAGES.es, ...MODULE_MESSAGES.es },
};

/** Traduit une clé avec interpolation `{var}` optionnelle. */
export function translate(
  lang: Lang,
  key: string,
  vars?: Record<string, string | number>
): string {
  const raw = MESSAGES[lang]?.[key] ?? MESSAGES.fr[key] ?? key;
  if (!vars) return raw;
  return raw.replace(/\{(\w+)\}/g, (_, name) =>
    vars[name] !== undefined ? String(vars[name]) : `{${name}}`
  );
}

/** Hook : t("clé") branché sur la langue live du hub Gandalf. */
export function useT() {
  const { lang } = useGandalf();
  return useCallback(
    (key: string, vars?: Record<string, string | number>) => translate(lang, key, vars),
    [lang]
  );
}

/** Hook : locale de formatage correspondant à la langue courante. */
export function useLocale(): string {
  const { lang } = useGandalf();
  return LANG_LOCALES[lang];
}
